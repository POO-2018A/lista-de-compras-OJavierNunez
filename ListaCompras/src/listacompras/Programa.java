    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        String producto = txtProducto.getText();
        
        if(producto.equals("")){
            JOptionPane.showMessageDialog(rootPane, "No has ingresado ningún item, vuelve a intentarlo");
        }else{
            unir = unir + producto + "\n";
            txtItems.setText(unir);
            JOptionPane.showMessageDialog(rootPane, "Compra registrada exitosamente!");
        }  
        
    }//GEN-LAST:event_btnAñadirActionPerformed

